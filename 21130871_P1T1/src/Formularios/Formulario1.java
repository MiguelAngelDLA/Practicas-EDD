    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() > 0){
            jTableBusqueda.setValueAt(this.jTable1.getValueAt(jTable1.getSelectedRow(), 0), 0, 0);
            jTableBusqueda.setValueAt(this.jTable1.getValueAt(jTable1.getSelectedRow(), 1), 0, 1);
            jTableBusqueda.setValueAt(this.jTable1.getValueAt(jTable1.getSelectedRow(), 2), 0, 2);
            this.jTableBusqueda.setValueAt(this.jTable1.getValueAt(jTable1.getSelectedRow(), 3), 0, 3);
            
            index = jTable1.getSelectedRow();
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        // TODO add your handling code here:
        try{
        String nombreModificado = jTableBusqueda.getValueAt(0, 0).toString();
        int edad = Integer.parseInt(jTableBusqueda.getValueAt(0, 1).toString());
        double estatura = Double.parseDouble(jTableBusqueda.getValueAt(0, 2).toString());
        String ocupacion = jTableBusqueda.getValueAt(0, 3).toString();
        
        Persona personaModificada = new Persona(nombreModificado, edad, estatura, ocupacion);
        
        oEj.modificarPersona(index, personaModificada);
        
        oEj.mostrarArreglo(jTable1);
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButtonModificarActionPerformed

