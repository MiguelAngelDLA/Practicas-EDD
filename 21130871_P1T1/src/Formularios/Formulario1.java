    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel)jTableOcupacion.getModel();
        int j = 0;
        for (int i = 0; i < oEj.getArrPersonas().length; i++){
            if(oEj.getArrPersonas()[i].getOcupacion().equals(jComboBox1.getItemAt(i))){
                j++;
                modelo.setRowCount(j);
                jTableOcupacion.setValueAt(oEj.getArrPersonas()[i].getNombre(), j, 0);
                jTableOcupacion.setValueAt(oEj.getArrPersonas()[i].getEdad(), j, 1);
                jTableOcupacion.setValueAt(oEj.getArrPersonas()[i].getEstatura(), j, 2);
                jTableOcupacion.setValueAt(oEj.getArrPersonas()[i].getOcupacion(), j, 3);
            }
        }
        jTableOcupacion.setModel(modelo);
        
    }//GEN-LAST:event_jComboBox1ItemStateChanged

