    private void jButtonPosicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPosicionActionPerformed
        // TODO add your handling code here:
        try{
            int posicion = Integer.parseInt(jTextFieldElemento.getText());
            stack.eliminarElemento(posicion);
            stack.mostrar(jTableMostrar);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Inserte un formato de numero valido");
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButtonPosicionActionPerformed

    private void jButtonAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAñadirActionPerformed
        // TODO add your handling code here:
        try{
            int posicion = Integer.parseInt(jTextFieldElemento.getText());
            int numero = Integer.parseInt(this.jTableAleatorio.getValueAt(0, 0).toString());
            String nombre = this.jTableAleatorio.getValueAt(0, 1).toString();
            double costo = Double.parseDouble(this.jTableAleatorio.getValueAt(0, 2).toString());
            
            Dato nuevoDato = new Dato(numero, nombre, costo);
            
            stack.agregaElemento(posicion, nuevoDato);
            stack.mostrar(jTableMostrar);
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, e);
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(this, e);

        }
    }//GEN-LAST:event_jButtonAñadirActionPerformed

