    private void jButtonPushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPushActionPerformed
        // TODO add your handling code here:
        try{
        int numero = Integer.parseInt(this.jTableAleatorio.getValueAt(0, 0).toString());
        String nombre = this.jTableAleatorio.getValueAt(0, 1).toString();
        double costo = Double.parseDouble(this.jTableAleatorio.getValueAt(0, 2).toString());
        
        Dato dato = new Dato(numero, nombre, costo);
        
        stack.push(dato);
        stack.mostrar(jTableMostrar);
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButtonPushActionPerformed

