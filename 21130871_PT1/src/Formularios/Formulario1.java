    private void jButtonModificarCoordenadasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarCoordenadasActionPerformed
        // TODO add your handling code here:
        try{
            int x = Integer.parseInt(JOptionPane.showInputDialog("Introduzca coordenada x", "entrada"));
            int y = Integer.parseInt(JOptionPane.showInputDialog("Introduzca coordenada y", "entrada"));
            int altura = Integer.parseInt(JOptionPane.showInputDialog("Introduzca altura", "entrada"));
            int ancho = Integer.parseInt(JOptionPane.showInputDialog("Introduzca coordenada ancho", "entrada"));
            
            tiposDeFiguras figuraNueva = tiposDeFiguras.valueOf(jComboTipoFigura.getSelectedItem().toString());
            figura.modificarFigura(jPanelFigura, figuraNueva, x, y, altura, ancho);
        }catch(HeadlessException | NumberFormatException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButtonModificarCoordenadasActionPerformed

    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        try{
            String nombre = (jTableAleatorio.getValueAt(0, 1).toString());
            int numero =(Integer.parseInt(jTableAleatorio.getValueAt(0, 0).toString()));
            Shape fig = figura.getFigura();
            
            FiguraGeometrica figuraAGuardar = new FiguraGeometrica(numero, nombre);
            figuraAGuardar.setFigura(fig);
            metodos.guardar(figuraAGuardar);
            
            metodos.mostrar(jTableMostrar);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed

