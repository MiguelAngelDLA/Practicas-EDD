    private void jButtonModificarElementoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarElementoActionPerformed
        // TODO add your handling code here:
        try{
            int x = Integer.parseInt(JOptionPane.showInputDialog("Introduzca coordenada x", "entrada"));
            int y = Integer.parseInt(JOptionPane.showInputDialog("Introduzca coordenada y", "entrada"));
            int altura = Integer.parseInt(JOptionPane.showInputDialog("Introduzca altura", "entrada"));
            int ancho = Integer.parseInt(JOptionPane.showInputDialog("Introduzca coordenada ancho", "entrada"));
            
            tiposDeFiguras figuraNueva = tiposDeFiguras.valueOf(jTableMostrar.getValueAt(jTableMostrar.getSelectedRow(), 1).toString());
            figura.modificarFigura(jPanelMostrar, figuraNueva, x, y, altura, ancho);
            
            int numFigura = Integer.parseInt(jTableMostrar.getValueAt(jTableMostrar.getSelectedRow(), 0).toString());
            String nombre = jTableMostrar.getValueAt(jTableMostrar.getSelectedRow(), 1).toString();
            
            FiguraGeometrica figuraAGuardar = new FiguraGeometrica(numFigura, nombre);
            figuraAGuardar.setFigura(figura.getFigura());

            metodos.modificar(jTableMostrar.getSelectedRow(), figuraAGuardar);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButtonModificarElementoActionPerformed

